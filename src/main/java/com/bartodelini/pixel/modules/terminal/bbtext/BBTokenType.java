package com.bartodelini.pixel.modules.terminal.bbtext;

/**
 * A <i>BBTokenType</i> is used to distinguish between parts of a bb text.
 *
 * @author Bartolini
 * @version 1.0
 */
public enum BBTokenType {
    
    /**
     * The left delimiter.
     */
    LEFT_DELIMITER,
    /**
     * The text part.
     */
    TEXT,
    /**
     * The right delimiter.
     */
    RIGHT_DELIMITER
}